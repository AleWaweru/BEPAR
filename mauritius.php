                        <p>A domestic company is used for conducting activities with Mauritius residents such as trading, consultancy services, investment holding, and acquisition of immovable property under the Property Development Scheme in Mauritius amongst others. A domestic company can also apply for a Freeport License for warehousing, transshipment and/or transformation of products in the Freeport Sector in Mauritius.</p>
