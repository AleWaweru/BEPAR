                    <h4>Limited by guarantee</h4>
                    <p>Company limited by guarantee means a company formed on the principle of having the liability of its members limited by its constitution to such amount as the members may respectively undertake to contribute to the assets of the company in the event of its being wound up</p>
                    <h4>Limited by both shares and guarantee </h4>
                    <p>Company limited by shares and by guarantee means a company formed on the principle of having the liability of its members (1) who are shareholders, limited to the amount unpaid, if any, (2) to those who have given a guarantee, limited to the amount they have undertaken to contribute.</p>
