                    <h3>Financial Year End and Annual Returns</h3>
                    <p>A company’s financial year end (FYE) represents the final day of its accounting period. The accounting period is the recognised interval to complete an accounting cycle of the business. The periodicity provides perspective about the profitability of the business on an ongoing basis. Records of transactions are kept over this period and reported in the form of financial statement.

                        Accounting periods can be 12 months or over 52 weeks. If you decide on a 12-month accounting period starting 1 January 2022, your company’s FYE will be 31 December 2022. But if you choose to have a 52-week accounting period starting 1 January 2022, your company’s FYE will be on 30 December 2020.

                        There are statutory obligations under the Companies Act that company directors must comply with or face enforcement action. Annual obligations such as holding annual general meetings and filing annual returns are examples of key statutory requirements.

                        Private companies must file their annual return within 7 months after the financial year end.

                        Filing your company’s annual return on time helps to ensure proper and timely disclosure to all stakeholders. All companies including inactive and dormant companies are required to file annual returns. As long as your company’s status is “live”, you must file its annual return with ACRA even if IRAS has exempted your company from filing its income tax return.
                    </p>
                    <h3>
                        Appointing Directors, Company Secretary and Other Key Personnel
                        Company Director

                    </h3>
                    <p>Every company must have at least one director who is locally resident in Singapore.</p>

                    <ol>
                        <h4>A company director must be:</h4>
                        <li>At least 18 years old</li>
                        <li>Of full legal capacity</li>
                        <li>A Singapore citizen, Singapore permanent resident, EntrePass or employment pass (EP) holder</li>
                        <li>Not disqualified from acting as a director of a company (e.g. an undischarged bankrupt)</li>
                    </ol>

                    <ol>
                        <h4>Company Secretary</h4>
                        <p>Companies must appoint a company secretary within 6 months from the date of incorporation. This position cannot be left vacant for more than 6 months or the directors may face a penalty of up to $1,000.</p>
                        <p>A company secretary must be:</p>
                        <li> A natural person</li>
                        <li>Locally resident in Singapore.</li>
                        <p>The sole director of a company and the company secretary cannot be the same person.

                        </p>
                    </ol>
                    <ol>
                        <h4>Auditor</h4>
                        <p>Unless your company is exempted from audit requirements under the relevant sections of the Companies Act, you must appoint an auditor within 3 months of incorporation.</p>
                        <p>Other key personnel that needs to be appointed are:</p>
                        <li>Chief Executive Officer (Optional)</li>
                        <li> Managing director (optional)</li>
                    </ol>
                    <h4>Requirements of Registered Office Addresses</h4>
                    <p>All companies must ensure that their registered office is open and accessible to the public for at least three hours during ordinary business hours on each business day. A business day is any day excluding Saturday, Sunday and public holidays.

                        The purpose of this requirement is to allow members of the public to reach out to the office if necessary and to facilitate the delivery of any legal documents. Companies and directors that fail to comply with this requirement may be fined up to S$5,000.

                        The registered office must be an address in Singapore, but it need not be the place of operation.
                    </p>

