                    <h4>Introduction</h4>
                    <h3>Before you register your Singapore company, you’ll need to be aware of these requirements: </h3>

                    <ol>
                        <li>Your company name needs to be approved before registration</li>
                        <li> You need to appoint a minimum of one resident director. </li>
                        <li>You can have anywhere between 1-50 shareholders, which may or may not be directors. Shareholders can consist of both local and non-local individuals or companies, and 100% non-local shareholding is allowed. </li>
                        <li> You need to appoint a qualified resident company secretary within 6 months of your Singapore company’s registration. Sole directors and/or shareholders cannot act as the company secretary.</li>
                        <li>You need to possess a minimum of S$1 worth of paid-up capital (also known as share capital) to register your Singapore company. This amount can be increased any time after your company is incorporated.</li>
                        <li>You need to provide a local, physical Singapore address as the registered address of the company. The registered address can be either a residential or commercial address, but not a P.O. Box.</li>
                        <li> Singapore-registered companies enjoy attractive tax exemptions and incentives. Your company pays less than 9% for the first S$300,000 you make in annual profits, followed by a flat rate of 17% flat thereafter. Singapore companies do not have to pay capital gains or dividend taxes. For further information on taxes, refer to our Singapore corporate tax guide</li>

                    </ol>

