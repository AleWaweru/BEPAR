                    <h3>Nigeria  Corporate services </h3>
                    <h4>Introduction</h4>

                    <p>Primarily, the Corporate Affairs Commission is given the responsibility of registering companies in Nigeria. The body was setup by the Companies and Allied Matters Act (CAMA). Company registration in Nigeria can now be completed online using the new e-registration portal launched by the Corporate Affairs Commission (CAC)..</p>


                    <h4>Key Facts and Requirements for Registering a Company in Nigeria</h4>
                    <ol>
                        <li>
                            Only one person can register a company and act as both director and shareholder where no foreigner will be involved in the composition of the company.
                        </li>

                        <li> Where foreigners will be involved, a minimum of two directors will be required. And where the shareholders are individual foreigners, a minimum of two persons are required to act as shareholders for the company.</li>
                        <li> A private company with only local director(s) is not mandatorily required to appoint a secretary at a point of incorporation, however, a company owned by foreigners or involving foreign participation must appoint a secretary at the point of incorporation.</li>
                        <li>Every company must have an address in Nigeria at the point of registration (registered office address).</li>
                        <li>Nigerian citizens can register a company with any authorized share capital amount; however, a foreign-owned company must have a minimum of Ten Million Naira authorized share capital to be incorporated.</li>
                        <li>Where an agent or a corporate lawyer is retained for a company registration, the applicant needs not to travel to visit the agent before the incorporation can be completed as the entire value chain can now be completed online.</li>


                    </ol>

                    <h4>Steps For Registering a Company In Nigeria</h4>
                    <ol>
                        <p>In registering a company in Nigeria, it's an easy process, which is supervised by CAC in their online portal. Below are steps on how companies are registered in Nigeria:</p>
                        <li>Search Available Company Name.</li>
                        <li>Reserve Company Name.</li>
                        <li>Complete all statutory forms.</li>
                        <li> Pay for filing fees.</li>
                        <li> Pay for stamp duty.</li>
                        <li>Submit the application for registration and register the company.</li>
                        <li> Once approved, the certificate and other incorporation documents can be downloaded online.</li>
                    </ol>

                    <ol>
                        <h4>
                            Documents to be Obtained after Incorporation
                        </h4>
                        <li>Incorporation Certificate, which shall include tax identification number (TIN) of the company.</li>
                        <li>Application for Registration Status of the company, which shall reflect the registered address, name of director(s), name of shareholder(s) and secretary of the company (if applicable) at the date of registration</li>
                        <li>Memorandum and Articles of Association (MEMART) of the company</li>
                    </ol>

                    <ol>
                        <h4>Post Incorporation Obligatons</h4>
                        <li>Registration for Companies Income Tax and Value Added Tax with the Federal Inland Services (FIRS)</li>
                        <li>Special Control Unit Agency Against Money Laundering (SCUML) Registration (If applicable</li>
                        <li>Every company in each year is to hold an Annual General Meeting (AGM)</li>
                        <li> Filing of annual returns 42 days after AGM</li>
                    </ol>
