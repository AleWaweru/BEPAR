                     <h3> Saint Lucia International Public Mutual Fund License</h3>

                    <h4>Introduction </h4>
                    <p>The International Mutual Funds Act Cap 12.16 (the Act) of the 2008 revised laws of St. Lucia provide for the regulation, authorization and control of international mutual funds and their administrators and managers, the carrying on of international mutual fund business in or from Saint Lucia and for related matters.
                        The Financial Services Regulatory Authority of Saint Lucia (FSRA) licenses Public Mutual Funds and registers Private Mutual Funds in Saint Lucia.

                    </p>

                    <h4>About International Public Mutual Fund </h4>
                    <p>International Public Mutual Funds are funds open to the public for investment. A public mutual fund can therefore be an index fund, a stock fund, a bond fund or a money market fund. They are managed by professional fund managers, who actively invest in various securities to achieve stated objectives, which can be for example, capital growth or income. Public mutual funds are monitored through the analysis of the companies’ audited accounts which is submitted to the FSRA three months after the financial year end. </p>
                    <h4>Legal Requirements</h4>
                    <h4>Categories</h4>
                    <h5>Entity Requirements</h5>
                    <p>Eligible company is a body corporate which is incorporated or continued under the 
                        International Business Companies Act, Cap. 12.14; for the purpose of carrying on 
                        international public mutual fund business and whose objects or 
                        business activities are restricted to international public mutual fund business.</p>

                    <h5>Director’s Requirements</h5>
                    <p>A public fund is required to appoint at least one director, who must be natural persons.</p>

                    <h5>administrator</h5>
                    <p>	• A fund administrator of an international public mutual fund must be a person who is granted and 
                        holds a valid international public mutual fund administrator licence issued by the minister. <br>
                        • The fund administrator must be a person who is an international business company incorporated.</p>
                    
                    <h5>Auditor</h5>
                    <p>• An auditor must be a member of the Institute of Chartered Accountants of the Eastern 
                        Caribbean or another professional accounting institute or association and who is approved as 
                        an auditor by the Minister; <br>
                        • The auditor must be independent of the international mutual fund or the administrator or the manager 
                        which he or she is auditing; and • The auditor must have at 
                        least 5 years of experience in performing audits of financial institutions or mutual funds.</p>
                        
                    <h5>Fund Manager</h5>
                    <p>	• A fund manager carrying out business as an international public mutual fund manager must be a 
                        person who is granted and holds a valid international public mutual fund manager licence and complies 
                        with the provisions of this Act, 
                        the Regulations and the terms and conditions of the international public mutual fund manager licence</p>
                        
                    <h5>Offering Memorandum</h5>
                    <p>	An international public mutual fund must have a current offering document filed by the director 
                        and which must provide full, true and plain disclosure of: <br>
                        a) the issue price of any equity interests being offered <br>
                        b) particulars of the business and finances of the international public mutual fund offering such equity interests; and <br>
                        c) all facts present or foreseeable that significantly affect or 
                        would reasonably be expected to have a significant effect on the disclosure required in a) above.</p>   
                        
                    <h4>Ongoing requirements</h4>
                    <ul>
                        <li> The prescribed annual fee shall be paid on or before the 15th day of January in each year following first licensing in respect of each international public mutual fund licence.</li>
                        <li> An international public mutual fund licensee shall have its accounts audited annually or at such other times as the Director may require by an auditor appointed by the international public mutual fund licensee who shall conduct the audit in accordance with international standards on auditing.</li>
                        <li>The audited accounts of the international public mutual fund licensee shall be forwarded to the Director within 3 months of the end of the financial year of the international public mutual fund licensee unless prior written approval for an extension has been granted by the Director</li>
                        <li> Where an international public mutual fund licensee changes its auditor, the administrator or manager shall immediately advise the Director of the change and as to the reason for the change and the Director if satisfied that the auditor meets the requirements of this Act shall record the change accordingly.</li>
                    </ul>


