                    <p>Seychelles International Business Company (IBC)</p>
                    <p>A Seychelles IBC company is incorporated under the International Business Companies Act 1994 and it is one of the most common and cost-efficient structures. Because of this, many international clients choose to set up an International Business Company for their offshore entities.</p>
                    <p>IBCs are not required to pay taxes in Seychelles on their foreign income and there is no stamp duty on the transfer of shares. A Seychelles IBC is only subject to a minimum government license fee of US$100 and 7.5% tax regardless of its authorized capital.</p>
                    <p>There are however a number of business activities that foreign investors are not allowed to conduct with their Seychelles IBCs such as business activities related to banking, and regulated financial services such as securities dealing, insurance, or reinsurance. </p>
