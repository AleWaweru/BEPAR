                     <h3>South Africa  Corporate services </h3>
                    <h4>Introduction</h4>

                    <p>The incorporation process is fairly straightforward in South Africa, taking just three to five days. Registration takes place with the Companies and Intellectual Properties Commission (CIPC) – this authority manages both the companies and the IP registration of the companies. It is therefore possible to create a company and register a website and intellectual property at the same time.</p>

                    <p>It is relatively easy to incorporate a Private Company; also known as Proprietary Limited Company (Pty Limited) in South Africa. Pty Limited can be established with a minimum of one shareholder and a maximum of fifty shareholders. Corporate entities can also be shareholders of South African Pty Limited. However, corporate directors are no longer permitted.</p>
                    <p>In comparison to a Public Company, private companies (Pty Limited) have lesser annual compliance obligations, but all companies are expected to file annual accounts and annual returns, irrespective of company size. They are also required to hold a general meeting every year.</p>
                    <p>If a company has public interest, a certain amount of turnover, a certain number of employees, or a certain amount of debt, they may also be called upon for a company audit to show enhanced compliance.</p>

                    <h3>Types of Companies</h3>
                    <h5>Private Company</h5>
                    <p>A private company may not offer shares to the public and restrictions are also placed on the transferability of their shares. Private companies must have at least one director and one incorporator. The director and incorporator may be the same person. The word “person” includes a juristic entity. This means that a legal entity or a trust may be an incorporator of a new company. Most private companies are owner-managed and tend to have a smaller number of directors.</p>
                    <h5>Public Company</h5>
                    <p>Public companies must have at least three directors. Only public companies may be listed on the Johannesburg Securities Exchange. Public companies must be audited and must produce audited financial statements which are tabled before shareholders annually. Depending on the size of the public company, the company may also be required to have an Audit Committee and a Social and Ethics Committee.</p>
                    <h5>Non-Profit Company</h5>
                    <p>A non-profit company is a company incorporated for public benefit or other objective relating to one or more of cultural, social activities, communal or group interest.</p>
                    <p>The income and property of a non-profit company is not distributed to its incorporators, members, directors, officers or persons relating to any of them and must be used to advance the purpose for which it was created, as set out in its memorandum of incorporation. A non-profit company must have at least three incorporators and three directors and may be registered with or without members. A non-profit company is not required to have members. The members of a non-profit company are persons who participate in the activities of the non-profit company, such as members of a church or a pension fund. Non-profit companies registered without members, may be registered with a standard or a customized Memorandum of Incorporation (MOI). There are three types of non-profit companies that can be registered at CIPC.</p>
                    <h5>Limited Liability Company</h5>
                    <p>The directors and past directors (where applicable) of personal liability companies are jointly and severally liable together with the company for any debts and liabilities arising during their periods of office.</p>
                    <h5>Foreign Company</h5>
                    <p>A foreign company is a company incorporated outside of South Africa, irrespective of whether it is a profit or non-profit company or carrying out business in South Africa. A foreign company is prohibited from offering securities to the South African public unless it follows the specific provisions of the Companies Act, 2008, relating to offers to the public.</p>
                    <p>A foreign company is required to register as an “external company” with CIPC if it conducts or intends to conduct business in South Africa. Section 23 of the Companies Act, 2008, lists a series of activities which will be regarded as conducting business.</p>
                    <p>This list includes the following:</p>
                    <ul>
                        <li>Holding a meeting or meetings of shareholders or board of the foreign company, or otherwise conducting the internal affairs of the company;</li>
                        <li>Establishing or maintaining any bank or other financial account;</li>
                        <li>Establishing or maintaining offices or agencies for the transfer, exchange or registration of the foreign company’s own securities;</li>
                        <li>Creating or acquiring any debts, mortgages, or security interests in any property;</li>
                        <li>Acquiring any interest in intellectual property; and</li>
                        <li>Entering into contracts of employment.</li>
                    </ul>

                    <h3>List of Documents needed to incorporate in South Africa</h3>
                    <p>The below listed documents will be required when incorporating a South African Company:</p>
                    <ol>
                        <li>Completed Notice of Incorporation form CoR14.1;</li>
                        <li>Completed forms CoR15.1A in the case of a standard private company or CoR15.1B for a customized private company;</li>
                        <li>A valid name reservation confirmation certificate CoR9.4;</li>
                        <li>Certified true copies of national ID/ passport of all individual shareholders and directors;</li>
                        <li>A Power of Attorney authorizing the company representative (if applicable);</li>
                        <li>Compete VAT 101 form to register the local entity for VAT with SARS;</li>
                        <li>Complete EMP101 form to register the local entity for PAYE with SARS;</li>
                        <li>Complete Was2 form to register for compensation for Occupational Injuries and Diseases (COID with the Department of Labor and Compensation Commissioner;</li>
                        <li>Complete UI8 to register employees for unemployment insurance with UIF.</li>
                    </ol>
                    <h3>Company Secretary</h3>
                    <p>All south African Companies must appoint a public officer, who will be an individual resident in the country.</p>
