                        <h4>Mauritius CIS Manager License Legal requirements </h4>
                        <h4>Categories</h4>
                        <h5>Entity Requirements</h5>
                        <p>The entity making the application is a Mauritius GBC 1 holding a Global Business Category One License (GBL1).</p>

                        <h5>Director’s Requirements</h5>
                        <p>	Legally speaking, a minimum of 2 directors are required for GBC 1 holding a special license such a CIS Manager license. 
                            However, the regulator’s and Mauritius Corporate Governance regime require that: <br>
                            a. Such an entity has got a well-balanced board in terms of experience, expertise and governance independence;  <br>
                            b. There needs to be fit and proper directors in different areas of running such a regulated Investment Business. 
                            Therefore, the 2 local directors required for the GBC 1 regime, almost always will not satisfy the regulator’s criteria. 
                            There will need to be an additional minimum 2 directors of different backgrounds, 
                            and who are fit and proper with experience in the investment industry.</p>

                        <h5>Shareholder requirements</h5>
                        <p>• The CIS Manger will need at least one shareholder. <br>
                            • Corporate shareholding is allowed. <br>
                            • 100% Foreign ownership is permitted.</p>

                        <h5>Mauritius Office</h5>
                        <p>• The applicant must satisfy the authorities that they have procured and equipped 
                            a place of business in Mauritius from where the operations of the company shall be run.</p>
                        
                        <h5>Auditor</h5>
                        <p>	• A CIS Manager is required to have its accounts audited annually by an auditor. <br>
                            • The applicant must put forward at the time of application, the designate auditor for approval by the authority. <br>
                            • The auditor must be a firm licensed in Mauritius</p>

                        <h5>Insurance</h5>
                        <p>• A CIS Manager is also required to have a Professional Indemnity cover that varies with the volume of activities and risk of its business. <br>
                            • An insurance quote must be submitted along with the application for approval by the authority. <br>
                            • Upon licensing, such insurance cover must be procured within the period set in the license conditions and a copy of the policy submitted to the regulator.</p>
                        
                        <h5>Minimum Capital Requirements</h5>
                        <p>• A CIS manager holding a licence issued by the Commission shall maintain a minimum stated 
                            unimpaired capital of at least Mauritian rupees one million or an equivalent amount.</p>

                        
                            

