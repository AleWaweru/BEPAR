                    <ul>
                        <h4>About Mauritius CIS Manager License </h4>
                        <p>The Securities (Collective Investment Schemes and Closed-end Funds) Regulations 2008 is the legal framework governing the provisions and setting the parameters within which a Collective Investment Scheme can be operated.
                            A CIS manager holding a licence issued by the Commission shall be a company which shall be incorporated and have its place of business in Mauritius, and is engaged solely in the business of management of CISs, unless otherwise authorised by the Commission.
                        </p>
                        <h4>Applicable Laws</h4>
                        <ul>
                            <li> Financial Services Act 2007 (31 Dec 2018)</li>
                            <li>Companies Act 2001 (Corporate and Business Registration Department)</li>
                            <li> Securities Act 2005</li>
                            <li>Securities (Collective Investment Schemes and Closed-end Funds) Regulations 2008</li>
                            <li> Code of Business Conduct (October 2015)</li>
                            <li> Prevention of Terrorism Act 2002</li>
                            <li> Financial Intelligence and Anti-Money Laundering Act 2002</li>
                            <li> Code on Prevention of Money Laundering and Terrorist Financing</li>
                            <li> Data Protection Act 2017</li>
                        </ul>

                    </ul>
                    <ul>
                        <h4>CIS Administrator Approval

                        </h4>
                        <p>A CIS manager or a collective investment scheme may appoint a CIS administrator to provide administrative services with respect to a collective investment scheme.
                            When seeking the approval of the Commission, the CIS manager or the collective investment scheme is required to:
                        </p>
                        <li> specify which administrative services the CIS administrator may provide;</li>
                        <li> give complete details of the CIS administrator, including its constitution, resources, past experience in providing such services, names of the collective investment schemes to which administrative services have been or are presently provided;</li>
                        <li> provide any other information required by the Commission;</li>
                        <li> include the fees specified in FSC Rules.</li>
                        <p>The Act requires the CIS manager or the collective investment scheme to seek prior approval of the Commission for a change of CIS administrator.</p>
                    </ul>

                    <ul>
                        <h4>Permitted Activities of a CIS Manager</h4>
                        <p>A CIS Manager may carry out any of the activities related to the management of a collective investment scheme, including:</p>
                        <li> all administrative services required by the scheme;</li>
                        <li>provision of registrar and transfer facilities;</li>
                        <li> distribution of the securities of the scheme;</li>
                        <li> maintaining accounting records of the scheme;</li>
                        <li> giving investment advice in relation to the scheme; and</li>
                        <li> managing the portfolio of the scheme.</li>
                    </ul>
                    <ul>
                        <h4>Licensing conditions for a Mauritius CIS Manager License</h4>
                        <p>The Financial Services Commission (“FSC”) shall not grant a licence for CIS Manager license unless it is satisfied that an application for a CIS manager licence filed with the Commission is accompanied by:</p>
                        <li> the constitutive documents of the applicant</li>
                        <li> a detailed description of how the applicant intends to comply with the Act, including any regulations and rules made thereunder;</li>
                        <li> information that demonstrates that it has suitably qualified staff with the appropriate expertise and experience to carry out the functions of a CIS manager;</li>
                        <li> the personal questionnaire form for every officer or proposed officer, controller, shareholder and beneficial owner of the CIS manager;</li>
                        <li> a list of persons responsible for the management of securities portfolios of the CISs under management and the competence of these persons;</li>
                        <li> the audited financial statements of the applicant as at the last balance sheet date; and
                            the relevant fees.
                        </li>
                    </ul>



