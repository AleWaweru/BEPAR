                         b) Have sufficient educational or other qualifications or experience;  <br>
                         c) Have sufficient authority within the company; <br>
                         d) Be a fit and proper person, to supervise the business for which the company that has nominated him is licensed or is applying to be licensed;</p>
                        
                         <h4>Authorized Representative</h4>
                        <p>
                        The Applicant must employ at least one individual who is licensed as a representative. The representative must: <br>
                        a) Have successfully completed the ECSM Certification Examination Program. <br>
                        b) Have sufficient educational or other qualifications or experience; <br>
                        c) Be a fit and proper person to be licensed as a representative; .</p>

