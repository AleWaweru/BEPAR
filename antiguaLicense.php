                                        a) A company, <br>
                                        b) Limited partnerships, <br>
                                        c) Unit trust or <br>
                                        d) Other business entity, which is incorporated, registered or otherwise established under the laws of a member territory of the Eastern Caribbean Currency Union.
                        </p>

                        <h4>Director’s Requirements</h4>
                        <p>
                        The licensee will need at least 2 (two) natural person Fit and Proper Directors of any jurisdiction.</p>

