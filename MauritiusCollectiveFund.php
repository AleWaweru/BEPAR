                        <p>Collective Investment Schemes and other non-bank financial services are regulated by The Financial Services Commission (FSC).
                            The regulator has stated that it aims to align itself with the practices and policies of countries that meet the highest international standards of regulation and supervision of non-banking financial services. As a member of international organizations, the FSC is bound by strict standards and practices thus preserving the stellar reputation of Mauritius in the international financial services sector. The Mauritius Financial Services Commission has their focus clearly on international policies in order to:
                        </p>
                        <li> Ensure that international standards and practices are adhered to in Mauritius.</li>

                        <li> Encourage cooperation between local and international regulators for effective supervision and control of financial systems and exchange of information for stability, fairness and efficient markets.</li>
