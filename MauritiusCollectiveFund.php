                        <h4>About Mauritius Global Funds</h4>
                        <p>Global funds registered with the Financial Services Commission (FSC) in Mauritius are commonly structured as companies incorporated under the Companies Act 2001 and licensed as a company holding a Category 1 Global Business Licence under the Financial Services Act 2007 (FSA). Such a structure, referred to as an Investment Company, is defined as one where the company’s business consists of investing its funds mainly in securities with the aim of spreading investment risk and giving members of the company the benefit of the results of the management of its funds.</p>
                        <li>Applicable Laws
