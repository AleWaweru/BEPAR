                        <h4>Closed-End Funds</h4>
                        <p>The provisions in the Securities Regulations relating to closed-end funds shall apply to all closed-end funds which are reporting issuers or where the closed-end fund is filing a prospectus with the intention of applying for a listing on a licensed securities exchange.
                            A closed-end fund shall appoint and have at all times a CIS manager, however where the closed-end fund is self-managed the board of directors of the fund shall be subject to all provisions relating to a CIS manager.
                        </p>
                        <h4>Licensing conditions for a Mauritius Global Funds License</h4>
                        <p>The Commission may grant an authorisation for a Global scheme provided that:</p>
                        <li> information relating to the CIS manager and the custodian is submitted with the application for authorisation;</li>
                        <li> a CIS administrator with a place of business in Mauritius is appointed;</li>
                        <li> the accounting and reporting services are carried out by the CIS manager, or the CIS Administrator of the scheme, having a place of business in Mauritius</li>


                        <h4>Legal Requirements </h4>
                        <h4>Categories</h4>
                        <h5>Entity Requirements</h5>
                        <p>The entity making the application is a Mauritius GBC 1 holding a Global Business Category One License (GBL1).</p>

                        <h5>Director’s Requirements</h5>
                        <p>Legally speaking, a minimum of 2 directors are required for GBC 1 holding a special license such a Global Fund license. However, the regulator’s and Mauritius Corporate Governance regime require that: <br>
                            a. Such an entity has got a well-balanced board in terms of experience, expertise and governance independence; <br>
                            b. There needs to be fit and proper directors in different areas of running such a regulated Investment Business. Therefore, the 2 local directors required for the GBC 1 regime, almost always will not satisfy the regulator’s criteria. There will need to be an additional minimum 2 directors of different backgrounds,
                            and who are fit and proper with experience in the investment industry.</p>

                        <h5>Shareholder requirements</h5>
                        <p>• The fund will need at least one shareholder. <br>
                            • Corporate shareholding is allowed and 100% foreign ownership is permitted.</p>

                        <h5>Auditor</h5>
                        <p>• A Global Fund is required to have its accounts audited annually by an auditor. <br>
                            • The auditor shall not be an officer of the custodian, the CIS manager or any support functionary of the CIS.</p>

                        <h5>Minimum funding</h5>
                        <p>• The prospectus of a scheme shall specify that the scheme must receive a minimum amount of subscription of at least 5% of the total amount to be raised from investors so as to begin operating the scheme. <br>
                            • Where the minimum amount of subscriptions indicated in the prospectus is not reached during the first six months of the offering period, the funds shall be returned to the investors together with any interest earned thereon, unless the CIS can justify a request for extension and the Commission agrees to such an extension, which shall not exceed a further six months.</p>

                        <h5>Custodian</h5>
                        <p>• CIS shall appoint and shall at all times have a custodian.</p>

                        <h5>Prospectus or offer document</h5>
                        <p>• The constitutive documents of a CIS shall form an integral part of the prospectus or offer document of a CIS. <br>
                            • The prospectus or the offering document shall include information that may be necessary to enable participants to make an informed judgement as to the investment proposed.</p>


