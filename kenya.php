                    <h3>Taxation Requirements</h3>
                    <h4>Value Added Tax (VAT)</h4>
                    <ul>
                        <li>VAT must be paid Monthly</li>
                        <li>VAT is mandatory if the business earns an annual income above Kshs. 5 million</li>
                        <li>VAT is levied on the consumption of taxable goods & services</li>
                        <li>Currently and in general the VAT rate is 16%</li>
                    </ul>

                    <h4>Turnover Tax</h4>
                    <ul>
                        <li>Turnover Tax must be submitted Quarterly</li>
                        <li>Turnover Tax is mandatory if the business earns an annual income between 500,000/= & Kshs. 5 million</li>
                        <li>Turnover Tax is an Indirect tax, chargeable at 3% of total sales</li>
                    </ul>

                    <h4>Corporation Tax</h4>
                    <ul>
                        <li>Corporation Tax is liable on the Company’s worldwide trading profits</li>
                        <li>The rate of Corporation Tax is currently 32.5%</li>
                        <li>It is advisable to seek the services of a taxation advice consultant to ensure compliance</li>
                    </ul>


