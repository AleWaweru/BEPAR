                    <h3>Reporting Requirements</h3>
                    <p>Every company must file an annual financial statement and report each year. The requirements vary according to your company status with more details required for a Public Company listed on the Nairobi Stock Exchange (NSE). </p>

                    <h3>Filing Annual Reports</h3>
                    <p>Companies are required to prepare and submit to the Registrar annual returns made up to a date not later than the date that is from time to time the company’s return date. The date of the return would be the anniversary date of the company’s incorporation or the date when the Company last lodged its returns if different from the incorporation date.</p>

                    <h3>Filing of Financial Statements.</h3>
                    <p>Directors of a company are required to lodge with the Registrar financial statements for each financial year. Public Companies and Companies Limited by Guarantee have 6 months from the end of the financial year to file their financial statements. While Private Companies have 9 months from the end of the financial year to file their financial statements. The statements are to be filed together with the accompanying reports. These are the Auditors’ and Directors’ reports.</p>
                    <p>Companies must also file:</p>
                    <ol>
                        <li>Changes in the structure of the company</li>
                        <li>Ultimate Beneficial Ownership of the company</li>
                    </ol>
