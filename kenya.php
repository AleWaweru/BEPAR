                            <h3>Kenyan  Corporate services </h3>
                    <h4>Introduction</h4>

                    <p>Incorporation of a company in Kenya is a straightforward process and takes about 4 weeks to finalize.</p>
                    <h3>Types of companies</h3>

                    <p>There are five types of companies:</p>
                    <ol>
                        <li>
                            <h4>Private company limited by shares</h4>
                            <p>This type of company has a share capital and the liability of each member is limited to the amount, if any, unpaid on their shares. A private company cannot offer its shares for sale to the general public and may not have more than fifty (50) members.</p>
                        </li>
                        <li>
                            <h4>Unlimited company</h4>
                            <p>This type of company has no limit on the liability of its members.</p>
                        </li>
                        <li>
                            <h4>Public limited company</h4>
                            <p>A public company has a share capital and limits the liability of each member to the amount unpaid on their shares. It may offer its shares for sale to the general public and may be listed as the securities</p>
                        </li>
                        <li>
                            <h4>Company limited by guarantee</h4>
                            <p>This type of company does not have share capital; its members are guarantors rather than shareholders. The liability of members is limited to the amount they agree to contribute to the company’s assets if it is liquidated.</p>
                        </li>
                        <li>
                            <h4>Foreign Companies</h4>
                            <p>This type of Company is a Branch of a Company registered outside of Kenya, that has established a place of business in Kenya (For more details, see PN/07 – Foreign Companies).</p>
                        </li>
                    </ol>
                    <h3>Required documents</h3>
                    <p>To incorporate a company the following documents should be lodged at the Companies Registry for registration:</p>

                    <ol>
                        <li>company registration form (Form CR1)</li>
                        <li>application for reservation of a company name (Form CR14)</li>
                        <li>memorandum of association</li>
                        <li>particulars of the directors and secretary (Form CR2and Form CR10</li>
                        <li>statement of nominal capital</li>
                        <li>articles of association (however if model articles are adopted in entirety, there is no need for a copy to be lodged at the Companies Registry for filing)</li>
                        <li>notification of the residential address of directors and company secretary (Form CR8 and CR12)</li>
                        <li>copy of the Kenyan national identification of each director and secretary or, a copy of the Kenyan foreigner certificate, or in the case of a director who is not a Kenyan citizen and is not resident in Kenya, a copy of that director’s passport,</li>
                        <li>a passport-sized photograph of each director and the company secretary (please see Note 11 below on the requirement to appoint a company secretary).</li>
                    </ol>

