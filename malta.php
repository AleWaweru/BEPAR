                             <h3>   Malta Corporate Services </h3>
                    <h4>Introduction </h4>
                    <h3>Types of companies </h3>
                    <ol>
                        <li>
                            <h4>Limited Liability Companies</h4>
                            <p>A limited liability company is the most common form of business entity in Malta. It may have the status of a public or private company.
                                A limited liability company is validly constituted in accordance with the Companies Act once a memorandum of association is entered into and subscribed by at least two persons or in case of a single member company- by one person and a certificate of registration is issued in respect thereof by the Registrar of Companies.
                            </p>

                        </li>
                        <li>
                            <h4>Private Or Public Company</h4>
                            <p>A private company is a company that must, by its memorandum or articles: </p>
                            <ul>
                                <li>• restrict the right to transfer its shares;</li>
                                <li>• limit the number of members to fifty; and</li>
                                <li>• prohibit any invitation to the public to subscribe for any shares or debentures of the company</li>
                                <p>A public company is a company which does not qualify as a private company. A public company may offer shares or debentures to the public but it may not issue any form of application for its shares or debentures unless the company is registered and the issue is accompanied by a prospectus</p>
                            </ul>
                        </li>
                        <li>
                            <h4>Private Exempt Companies</h4>
                            <p>A private company may have the status of an exempt company, and qualify for certain advantages if the following conditions are contained in its memorandum or articles of association:</p>
                            <ul>
                                <li>• the number of persons holding debentures of the company is not more than 50; and </li>
                                <li>• no body corporate is a director of the company, and neither the company nor any of the directors is party to an arrangement whereby the policy of the company is capable of being determined by persons other than the directors, members or debenture holders thereof</li>

                        </li>
                        <p>Other types of companies are:</p>
                        <li>• Partnership Em Nom Collectif</li>
                        <li>• Redomiciliation of Companies</li>
                        <li>• Overseas Companies</li>
                        </ul>
                        <li>

                            <h4>Time Required for Incorporation</h4>
                            <p>The length of time to incorporate a company depends on the type of company being incorporated and on whether all information and documentation is available and in order. Once the Registrar has all necessary documentation and information, the process may take from as little as 24 hours.</p>
                        </li>
                        <li>
                            <h4>Registered Office </h4>
                            <p>Every company registered in Malta must have a registered office in Malta. This may be at the office of a local firm of lawyers, accountants or other providers of corporate services. Any changes to the company’s registered office must always be notified to the Registrar of Companies</p>

                        </li>
                        <li>
                            <h4>Objects</h4>
                            <p>The Memorandum of Association must specify the objects for which the company is set up. The objects may not be simply stated to be any lawful purpose or trade in general, unless a partnership is being incorporated </p>
                        </li>
                        <li>
                            <h4>Capital Requirements</h4>
                            <p>The minimum authorised share capital of a public company is EUR 46,587.47. In the case of a private company, the minimum authorised share capital is EUR 1,164.69. The authorised share capital shall be subscribed by at least two persons. Nonetheless in case of a single member company, share capital shall be prescribed by only one person, being the sole shareholder of the company.
                                Where the authorised share capital is equal to the minimum stipulated by law, as aforesaid, it must be fully subscribed in the memorandum. Where it exceeds such minimum, at least that minimum shall be subscribed in the memorandum.
                            </p>
                            <p>In the case of a public company, not less than 25%, and in the case of a private company, not less than 20%, of the nominal value of each share taken up shall be paid up on the signing of the memorandum. To evince this, a bank deposit slip must be brought with the memorandum and articles of association for new company formation.</p>

                        </li>
                        <li>
                            <h4>Directors and Company Secretary</h4>
                            <p>Every public company must have at least two directors whereas every private company must have at least one director.
                                Every company must have a company secretary. No company may have:
                            </p>
                            <ul>
                                <li>• as company secretary its sole director unless the company is a private exempt company.</li>
                                <li> as sole director of the company a body corporate, the sole director of which is company secretary to the company.</li>
                            </ul>
                            <p>It shall be the duty of the directors of a company to take all reasonable steps to ensure that the company secretary is an individual who appears to them to have the requisite knowledge and experience to discharge the functions of company secretary. The law does not require that the company secretary be resident in Malta. A company secretary may also be a duly registered company service provider in terms of the Company Service Providers Act.</p>
                        </li>
                        <li>
                            <h4>Company Returns & Annual Accounts</h4>
                            <p>All companies must prepare an annual return in the prescribed format to be made up, upon each anniversary date of its registration. The return must be filed with the Registrar of Companies within 42 days after the date to which it is made up. A payment between EUR100 and EUR1,400 depending on the authorised capital is to be submitted along with the return. </p>
                            <p>Companies are also required to file a copy of the annual accounts. These must generally be accompanied by a copy of the auditors’ report thereon, and the directors’ report unless a declaration Form is submitted in which case the letter is unnecessary. The annual accounts must be approved within 10 months from the end of the financial year, with a subsequent grace period of 42 days.
                                Companies returns, and annual accounts can also be filed online. As stated before this can be done by registering oneself as an “authorized agent” which gives one permission to file such documents electronically without the need to submit such documentation physically at the Registry of Companies.
                            </p>
                        </li>
