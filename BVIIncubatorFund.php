                    <ol>
                        <p>The incubator fund is designed for managers who are looking to start an investment strategy on a trial basis with sophisticated investors. If the strategy succeeds within the prescribed timescale, the Regulations provide a seamless route for the fund to be recognised or approved as a private professional or approved fund, depending on the manager's future plans for growth of the fund. If the strategy does not succeed, the regime allows for an incubator fund to close down or convert to an ordinary company. The key differentiating criteria for incubator funds over other fund types include:</p>
                        <ul>
                            <li> Only suitable for "sophisticated private investors". Such investors are defined in the Regulations as "a person who has been invited to invest in an incubator fund and the amount of his or her initial investment is not less than US$20,000."</li>
                            <li> The total number of investors is limited to 20. </li>
                            <li>Net assets of the fund must not exceed US$20,000,000 or its equivalent in any other currency. </li>
                            <li> No manager, administrator or custodian is required to be appointed.</li>
                            <li> No audit required.</li>
                            <li> Can only be approved for a two year period. It is possible to seek an extension from the Commission for an additional 12 month period. Prior to the end of this period it will be necessary to make an application to the Commission for the fund to be recognised as either a private fund or a professional fund or to be approved as an approved fund.</li>
                        </ul>


