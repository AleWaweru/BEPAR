                        <h3>Eligibility Criteria</h3>
                        <h4>Custodian</h4>
                        <p>• An incubator fund is required to appoint a custodian who must be functionally independent from the manager and the administrator. <br>
                            • The custodian must be a body corporate who is a fit and proper person to act as an authorised custodian of bearer shares; and has systems and procedures in place;  for the secure custody of bearer shares; and  that will enable it to comply with the obligations imposed on an authorised custodian. <br>
                            • A fund may apply for an exemption to engage a custodian, but in such event the FSC will want to be informed about the custodianship arrangements for the fund (for instance, whether a prime broker or the fund’s directors will be responsible for the safekeeping of the fund’s assets).</p>

                        <h4>Auditor</h4>
                        <p>	• An incubator fund is required to appoint, and at all times have, an auditor for the purposes of auditing their financial statements. <br>
                            • The auditor of an incubator fund does not need to be based in the BVI.</p>

                        <h4>Authorised Representative</h4>
                        <p>	• An incubator fund must appoint an authorized representative in the BVI. The authorised representative will act as a conduit between the fund and the FSC. <br>
                            • The authorized representative must hold a certificate issued by the FSC.</p>

                        <h4>Investment manager</h4>
                        <p>• A BVI domiciled investment manager can either be licensed under Part I of SIBA or be approved under the Investment Business (Approved Managers) Regulations, 2012 as amended (the “AMR”). <br>
                            • If the Investment Manager is licensed under SIBA, the incubator fund has to obtain a license Category 3, sub-category B (and possibly E). Under this regime each director, shareholder (holding a significant interest of 10%) and officer of the person seeking a license must satisfy the FSC’s fit and proper criteria. The application also requires a detailed business plan. Multiple ongoing requirements are in place, such as the appointment of an anti-money laundering reporting officer and compliance officer, appropriate compliance systems and controls, the preparation of financial statements in accordance with certain accounting standards and the appointment of an authorized representative. <br>
                            • If licensed under the AMR which is a lighter regulation and licensing process whereby the manager may carry on business 7 days after submitting the application form to the FSC, without having to wait for the outcome. After being approved under the AMR, an approved investment manager may serve an unlimited number of professional funds recognized under SIBA, being restricted only by the amount of aggregated assets under management, which for open-ended funds cannot exceed USD 400 million. An approved manager may also manage funds domiciled outside of the BVI, provided that these funds will meet similar criteria as private funds. <br>
                            • An approved manager must appoint an anti-money laundering reporting officer.</p>

