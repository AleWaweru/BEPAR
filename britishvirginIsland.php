                        <p>It is not legally required for the directors or shareholders to be of any specific nationality. Making IBCs very straightforward and easy to incorporate in the BVI.
                            Offshore companies are popular in the BVI as it features no minimum capital requirements, exemption from income tax, no need for annual reporting or auditing as well as no capital gains or withholding taxes. However, offshore companies cannot conduct business in the BVI at all.
                            There is no residency requirement for the shareholders/ directors and company secretary.
                        </p>
                        <p>It is important to note that the procedure for company registration in BVI is quite straightforward but the company legislation is constantly amended, it should be monitored and taken into account at all times. The last amended rules have been made stricter in the identification of investors coming to the jurisdiction.
                            Now, to complete the registration of a company in the British Virgin Islands, the beneficial owners must provide bank statements (resident bank) dating at least six months. In addition, confirmation of the registered address of the founders and their personal data is required.
