                        <h4>Economic Substance Requirements in The BVI</h4>
                        <p>Furthermore, The BVI’s Economic Substance (Companies and Limited Partnerships) Act, 2018 (BVI Substance Act) came into force on 1st January 2019. The BVI Substance Act is supplemented by the BVI’s Economic Substance Code (BVI Substance Code). This law applies to the following legal entities, they include:</p>
                        <li>• Companies and foreign companies incorporated/registered under the BVI Business Companies Act, 2004 (as amended), excluding companies which are not resident in the BVI; and</li>
                        <li>• Limited partnerships and foreign limited partnerships formed/registered under the Partnership Act, 1996 or the Limited Partnership Act, 2017, excluding limited partnerships which are not resident in the BVI or do not have a legal presence;</li>
                        <p>The following legal entities are exempted from the act, they include:</p>
                        <li>• An investment fund (within the meaning of applicable BVI legislation); or</li>
                        <li>• A non-resident company and a non-resident limited partnership.</li>
                        <p>An entity is a “non-resident company” or “non-resident partnership” if the company/partnership is resident for tax purposes in a jurisdiction outside the BVI which is not on Annex 1 to the EU list of non-cooperative jurisdictions for tax purposes.</p>
                        <p>Each legal entity which is not a tax resident outside the BVI (other than a pure equity holding entity) must, in relation to any relevant activity, carry out defined core income-generating activities in the BVI and demonstrate economic substance by reference to the following criteria with with regards to the nature and scale of the relevant activity:</p>
                        <li>• The relevant activity being directed and managed in the BVI</li>
                        <li>• Adequate numbers of suitably qualified employees who are physically present in BVI (whether they are employed by the relevant legal entity or not and either on temporary or long-term contracts);/li>
                        <li>• Adequate expenditure being incurred in the BVI;</li>
                        <li>• Appropriate physical offices or addresses in the BVI; and</li>

                        <li>• The relevant activity is an intellectual property business requiring the use of specific equipment located in the BVI.</li>

