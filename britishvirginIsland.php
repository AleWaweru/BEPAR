                        <li>• International Business Company and shelf corporations (both available).</li>

                        <li>• The company limited by guarantee;</li>

                        <li>• Hybrid company (limited by guarantee but has the right to issue shares);</li>
                        <li>• Unlimited company (without the right to issue shares);</li>
                        <li>• A segregated portfolio company (Protected cell company).</li>
                    </ol>
