                        <h4>Minimum Setup Requirements to Register a Company in the BVI</h4>
                        <p>Incorporation requirements of a Business Company, or commonly known as an International Business Company (IBC) are as followed:</p>
                        <li>• Director – 1 </li>
                        <li> • Shareholder – 1 </li>
                        <li>• Company Secretary – 1</li>
