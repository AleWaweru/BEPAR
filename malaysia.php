                    <h3>Malysia  Corporate services </h3>
                    <h4>Introduction</h4>

                    <p>For the registration of all businesses – including foreign companies – in Malaysia, the process of setting up a business entity is governed by the registrar of business commonly known as the Suruhanjaya Syarikat Malaysia (SSM) or Companies Commission of Malaysia.

                    </p>


                    <h4>The few incorporation options to choose from include:</h4>
                    <ol>
                        <li><strong> Sendirian Berhad (SDN BHD), a Private Limited Company, or Berhad (BHD), a Public Limited Company:</strong>
                            <p>
                                These are companies limited by shares, which requires a minimum of one shareholder (Section 14 of the Companies Act 2016), at least one company director (Section 122 of the Companies Act 2016), and a company secretary. The documents to be lodged with SSM include a company constitution, Section 201 (statutory declaration by the company director(s) of not being either bankrupt or convicted of any offense), Section 236(3) (declaration by a person before appointment as Company Secretary), and identity cards/passports of all the director(s) and the company secretary. The main difference between SDN BHD and BHD is that the former cannot have more than 50 shareholders and its shares are not open to the public, unlike the latter. In Malaysia, there is also the option of incorporating a company unlimited, whose incorporation procedure and documents required are the same as a company limited by shares. The only difference is, that the constitution of an unlimited company must state that the liability of its members is unlimited.
                            </p>

                        </li>
                        <li><strong> Sole Proprietorship or Partnership:</strong>
                            <p>
                                These can include any form of trade, commerce, profession, or activity done for profit, excluding those specified in the Schedule of the Registration of Businesses Act 1956 (ROBA 1956) and ROBA Rules 1957. If the business is entirely owned by an individual under his or her personal name, it is called Sole-proprietorship. If the business is owned by two or more persons but not exceeding 20 persons, it is known as a Partnership. The registration (for one to five years) can either be done at the SSM counter or online through the Ezbiz portal ezbiz.ssm.com.my and must be done within 30 days of the starting of the business.
                            </p>

                        </li>
                        <li><strong> Limited Liability Partnership (LLP):</strong>
                            <p>
                                This is an alternative business vehicle – formed by professionals such as Lawyers, Chartered Accountants, and Company Secretaries – regulated under the Limited Liability Partnerships Act 2012. Its salient features include flexibility in terms of formation, maintenance, and termination; protection of limited liability to its partners (meaning any debts are borne by the assets of the LLP and not that of its partners); and legal status of a body corporate capable of suing and being sued.
                            </p>

                        </li>

                    </ol>

                    <ol>
                        <h3>Company Features</h3>
                        <li> Company shall have name;</li>
                        <li> One or more members;</li>
                        <li>In the case of a limited company with shares, one or more shares; and</li>
                        <li> One or more directors</li>
                    </ol>


                    <ol>
                        <h3>
                            Requirements for Company Incorporation
                        </h3>
                        <p>The incorporation of companies under the Companies Act 2016 may be made by individuals who intend to form a company. The basic requirements are:</p>
                        <li> Private company - at least one (1) director who ordinarily resides in Malaysia by having a principal place of residence in Malaysia and one (1) promoter; or</li>
                        <li>Public company - at least two directors who ordinarily reside in Malaysia by having a principal place of residence in Malaysia and minimum one promoter</li>
                    </ol>

                    <ol>
                        <h3>Company Incorporation Procedures</h3>


                        <li> Requirement for approval of company name from SSM.</li>
                        <li>The company's incorporation application is online using one of the two (2) incorporation methods
                            <ul>
                                <li>
                                    Direct incorporation – combination of name reservation and incorporation process; or
                                </li>
                                <li>Name reservation - submitting the incorporation application within 30 days or such longer period as the Registrar may allow (maximum of 180 days) after the name is approved.</li>
                            </ul>
                        </li>
                        <li>Incorporation information that needs to be completed is as follows
                            <ul>
                                <li> The proposed company name</li>
                                <li>The status of a private or public company</li>
                                <li> The proposed type of business</li>
                                <li>The address of registered office</li>
                                <li> The business address</li>
                                <li> Complete details of directors(s) and promoter(s)</li>
                                <li>Declaration from directors(s) and promoter(s)</li>
                                <li> Declaration of compliance from individuals responsible for incorporation</li>
                                <li> Additional Documents (if any)</li>
                            </ul>
                        </li>
                    </ol>

                    <ol>
                        <h3>Annual Compliance</h3>
                        <p>Currently, the Company Act 2016 has eased the annual compliance requirement for private limited Company (Sdn. Bhd.). All private limited Company (Sdn. Bhd.) local and foreign owned alike must abide to the annual compliance filing as below:</p>
                        <li>File annual return according to the Company anniversary date – date of when the Company was initially incorporated. Example: The Company was incorporated in 21.07.2018, the first annual return must be filed by 21.07.2019 or before 20.08.2019 (30 days). Late filing penalty may be imposed if the filing is done after 30 days.</li>
                        <li>Lodge unaudited financial statement with the Companies Commission of Malaysia – Suruhanjaya Syarikat Malaysia (SSM). This type of business entity is exempted from preparing an audited report if they meet one of the criteria:
                            <uL>
                                <li> Dormant companies</li>
                                <li>Zero-revenue companies</li>
                                <li>Threshold-qualified companies – Revenue < MYR 100,000, total assets < MYR 300,000 and employee < 5 people</li>
                            </uL>
                        </li>

                        <li>Tax Structure in Malaysia
                            <p>In Malaysia, the corporate tax structure is territorial. Residents and non-residents are taxed on their Malaysian-sourced income while any foreign-sourced income is normally not taxable. This is applicable to both local and foreign owned Companies alike.
                                Based on the year assessment (YA) 2020, the tax rate is as follows:
                            </p>
                            <p>Company with paid-up capital < than MYR 2.5 million</p>
                        <li>On the first MYR 600,000 profit is taxed at 17%</li>
                        <li>Subsequent balance will be taxed at 24%</li>
                        </li>
                        <p>However, for Company with paid-up capital > than MYR 2.5 million, the tax rate is flat at 24%.</p>
