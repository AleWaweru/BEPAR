                    <h3>Hong Kong  Corporate services </h3>
                    <h4>Introduction</h4>

                    <p>Governed by the Companies Ordinance of Hong Kong, any person (foreign or local) above the age of 18 can setup a Hong Kong company. The most common type of business entity registered in Hong Kong is a private limited liability company.
                        Incorporation of a local limited company in Hong Kong
                    </p>


                    <h3>Types of Companies</h3>
                    <ol>
                        <li> Company limited by shares - the liability of members is limited by the articles of association to the amount unpaid on the shares respectively held by them</li>
                        <li>Company limited by guarantee - no share capital and the liability of members is limited by the articles of association to the amount that the members respectively undertake to contribute to the assets of the company in the event of its being wound up. Non-profit-making organisations are usually registered as guarantee companies.</li>

                    </ol>

                    <h4>Basic requirements of setting up a Company in Hong Kong</h4>
                    <ol>

                        <li>Company name – The company name must be approved before you can proceed with the incorporation of a Hong Kong company. </li>
                        <li>Companies Ordinance – With the amendment, effective 1 March 2018, every company incorporated in Hong Kong, except for those listed in the Hong Kong Stock Exchange (HKSE), will be required to create and maintain a register of all persons who have significant control of the company.</li>
                        <li>Designated Representative (DR) - All companies must also appoint a Designated Representative (DR) responsible for reporting in relation to the SCR to prescribed local authorities upon demand. </li>
                        <li>Directors – A minimum of one individual director and unlimited maximum number of directors allowed. The director must be a natural person who can be of any nationality and need not be resident in Hong Kong. Directors must be at least 18 years of age and must not be bankrupt or convicted for any malpractices. There is no requirement for the directors to also be shareholders. Nominee corporate directors can also be appointed in addition to the individual director. Directors Board meetings can be held anywhere in the world.</li>
                        <li> Shareholders – A Hong Kong private limited company can have a minimum of 1 and maximum of 50 shareholders. There is no residency requirement for shareholders. A director and shareholder can be the same or different person. The shareholder must be at least 18 years of age and can belong to any nationality. The shareholder can be a person or a company. 100% local or foreign shareholding is allowed. Appointment of nominee shareholders is permitted. Shareholders meetings can be held anywhere in the world.</li>
                        <li>Company Secretary – When setting up a company in Hong Kong, appointing a company secretary is mandatory. The secretary, if an individual, must ordinarily reside in Hong Kong; or if a body corporate, must have its registered office or a place of business in Hong Kong. It has to be noted that in case of a sole director/shareholder, the same person cannot act as the company secretary. The company secretary is responsible for maintaining the statutory books and records of the company and must also ensure the company’s compliance with all statutory requirements. A nominee secretary can be appointed.</li>

                        <li>Registered Address – In order to register a Hong Kong company, you must provide a local Hong Kong address as the registered address of the company. The registered address must be a physical address and cannot be a PO Box.</li>
                        <li> Public Information – Information about company officers viz. directors, shareholders and company secretary is public information as per Hong Kong Company Laws.</li>
                        <li>It is mandatory to file details of the company officers with the Hong Kong Companies Registry. If you wish to maintain confidentiality you can appoint a corporate shareholder and nominee individual director by utilizing the services of a professional services firm.</li>
                        <li>Taxation – Corporate tax, (or profits tax as it is called), is set at 16.5% of assessable profits for companies setup in Hong Kong. Hong Kong follows a territorial basis of taxation i.e. only profits which arise in or derived from Hong Kong are subject to tax in Hong Kong. There is no capital gains tax, withholding tax on dividends, or GST/VAT in Hong Kong. For more details on corporate taxes, refer to Hong Kong Corporate Tax guide.</li>
                        <li>Ongoing Compliance – It is mandatory for companies to prepare and maintain accounts. Accounts must be audited annually by Certified Public Accountants in Hong Kong. The audited accounts together with tax return must be filed annually with the Inland Revenue Department. Every company is required to file annual returns with the Companies Registry and pay the annual registration fee.</li>
                        <li>The Business Registration Certificate should be renewed, one month before expiry on an annual basis or once every three years, as the case may be.</li>
                        <li>An Annual General Meeting (AGM) should be held annually very calendar year. The AGM should be held within 18 months of the date of incorporation, after which no more than 15 months can elapse between one AGM and the next. A written resolution in lieu of Annual General Meeting is permissible. For more details, refer to Annual Compliance for Hong Kong Companies guide.</li>
                    </ol>



