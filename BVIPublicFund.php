                    <h3>BVI Public Fund License</h3>

                    <h4>Introduction </h4>

                    <p>The statutory legislation governing most funds in the BVI is the Securities and Investment Business Regulations 2015 (“SIBA”) and the Mutual Fund Regulations, 2010.
                    </p>
                    <h4>About BVI Professional Fund License</h4>


                    <ol>
                        <p>A fund which will not be “recognized” as a professional fund or private fund must apply for “registration” as a public fund under SIBA. In order to be registered, the public fund must demonstrate to the FSC that it satisfies the requirements of a public fund as set out in SIBA.






                        <h4> Functionaries’ requirements</h4>
                        <p>BVI funds, recognized or registered under SIBA, are generally required to appoint functionaries who must either meet the FSC’s ‘fit and proper’ criteria, or must be located in one of the BVI’s recognized jurisdictions. Functionaries from a non-recognized jurisdiction may also be appointed, provided that they will satisfy the FSC that their jurisdiction has an effective regulation on investment businesses in place. The below functionaries need to be appointed for a public fund.</p>

                        <h3>Eligibility Criteria</h3>

                        <h4>Director</h4>
                        <p>• A public fund is required to appoint at least two directors, who must be natural persons</p>

                        <h4>Administrator</h4>
                        <p>• A public fund must appoint an Administrator who will typically oversee the day-to-day operations of the fund,
                            calculate and determine the net asset value (“NAV”) of the fund, process subscriptions and redemptions of the fund,
                            act as the registrar and transfer agent, keep various records of the fund and undertake anti-money laundering procedures on behalf of the fund. <br>
                            • There is no requirement for the administrator chosen to be based in the BVI.</p>

                        <h4>Custodian</h4>
                        <p>• A public fund is required to appoint a custodian who must be functionally independent from the manager and the administrator. <br>
                            • The custodian must be a body corporate who is a fit and proper person to act as an authorised custodian of bearer shares; and has systems and procedures in place; <br>
                            • for the secure custody of bearer shares; and <br>
                            • that will enable it to comply with the obligations imposed on an authorised custodian. <br>
                            • A fund may apply for an exemption to engage a custodian, but in such event the FSC will want to be informed about the custodianship arrangements for the fund (for instance, whether a prime broker or the fund’s directors will be responsible for the safekeeping of the fund’s assets).</p>

                        <h4>Auditor</h4>
                        <p>• A public fund is required to appoint, and at all times have, an auditor for the purposes of auditing their financial statements. <br>
                            • The auditor of a Professional fund does not need to be based in the BVI.</p>

                        <h4>Authorised Representative</h4>
                        <p>• A public fund must appoint an authorized representative in the BVI. The authorised representative will act as a conduit between the fund and the FSC. <br>
                            • The authorized representative must hold a certificate issued by the FSC.</p>

                        <h4>Investment manager</h4>
                        <p>• A BVI domiciled investment manager can either be licensed under Part I of SIBA or be approved under the Investment Business (Approved Managers) Regulations, 2012 as amended (the “AMR”). <br>
                            • If the Investment Manager is licensed under SIBA, the professional fund has to obtain a license Category 3, sub-category B (and possibly E). Under this regime each director, shareholder (holding a significant interest of 10%) and officer of the person seeking a license must satisfy the FSC’s fit and proper criteria. The application also requires a detailed business plan. Multiple ongoing requirements are in place, such as the appointment of an anti-money laundering reporting officer and compliance officer, appropriate compliance systems and controls, the preparation of financial statements in accordance with certain accounting standards and the appointment of an authorized representative. <br>
                            • If licensed under the AMR which is a lighter regulation and licensing process whereby the manager may carry on business 7 days after submitting the application form to the FSC, without having to wait for the outcome. After being approved under the AMR, an approved investment manager may serve an unlimited number of professional funds recognized under SIBA, being restricted only by the amount of aggregated assets under management, which for open-ended funds cannot exceed USD 400 million. An approved manager may also manage funds domiciled outside of the BVI, provided that these funds will meet similar criteria as private funds. <br>
                            • An approved manager must appoint an anti-money laundering reporting officer.</p>


