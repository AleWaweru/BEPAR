                            <ul>

                                <li> A Public Fund must pay its annual mutual fund fee to the FSC by 31 March in each year.

                                </li>
                                <li> A Public fund must file its statistical return with the FSC by 30 June in each year (“Return”). The Return may be completed by any director, officer or other authorised representative of the reporting fund. </li>
                                <li> A Public fund must provide a copy of its annual financial statements to the FSC within six months after the end of its financial year or such extended period, not exceeding 15 months, as the FSC may approve in writing.
                                </li>
                                <li> a public fund must at all times have an adequate number of directors, being at least two (only an individual may be appointed as a director of a public fund) who are capable of exercising independent judgment, have sufficient time and commitment to undertake their duties and have sufficient knowledge, skills, experience and understanding of the fund’s business to ensure that the governing body is able to fulfil its obligations.</li>

                                <li> A Public fund must appoint and at all times have an auditor for the purpose of auditing its financial statements.</li>

                                <li> A Public fund must disclose to the FSC any matter that might reasonably be expected to have a significant regulatory impact including the suspension of valuation, dealing or redemptions, any matter that could impact on the ability of the fund to continue to carry on business and any incidence of fraud or criminal activity which is material to the safety, soundness or reputation of the fund.</li>

