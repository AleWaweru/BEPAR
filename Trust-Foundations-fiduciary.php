                            <h3>Trust</h3>

                            <p>A trust is a fiduciary relationship where a person (the settlor) gives/ bestows upon another person (the trustee) the right to hold title to the property for the benefit of a third party (the beneficiary). Thus, it can be used as another method of estate transfer.</p>

                            <h3>Parties to a Trust</h3>
                            
                            <ol>
                                <li>Settlor: This refers to the party who creates the trust by transferring his assets to the trustees. There is no legal obstacle stopping a settlor from appointing himself/herself as a trustee.</li>
                                <li>Trustee(s): This is a legal person who receives the assets transferred by the settlor and is required to administer the assets for the benefit of the beneficiaries.</li>
                                <li>Beneficiaries: These refer to persons entitled to use or enjoy the assets or income of the trust. A settlor cannot benefit from the trust property unless he or she is a beneficiary. However, nothing prevents a settlor from being a beneficiary.</li>
                            </ol>
                            
                            <h3>Why Establish a Living Trust</h3>
                            <ol>
                                <li>To ensure the settlor’s Property is distributed as per the settlor’s wishes and to avoid family squabbles after the death of the Settlor.</li>
                                <li>To avoid the lengthy and tedious probate process in court. A living trust has ownership rights over the assets held therein which assets can therefore not be distributed through the probate process.</li>
                                <li>To ensure the settlor’s Property is distributed as per the settlor’s wishes and to avoid family squabbles after the death of the Settlor.</li>
                                <li>To avoid or reduce inheritance or estate taxes depending on the jurisdiction of the Trust.</li>
                                <li>To maintain the privacy of the Settlor’s assets. The creation of a living trust protects the privacy of the Settlor since it avoids the probate process which is done in open court. As such, the assets and debts of the deceased do not become a matter of public record.</li>
                                <li>Settlor’s incapacitation- In case the settlor is incapacitated during their lifetime, a successor trustee can be appointed to manage the settlor’s assets thereby avoiding family squabbles.</li>
                                <li>To ensure that the Settlor retains control over his/her estate.</li>
                            </ol>
                            
