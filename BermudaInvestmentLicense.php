                            b) a company limited by guarantee that is not authorised to issue shares; <br>
                            c) a company limited by guarantee that is authorised to issue shares;</p>

                        <h5>Director’s Requirements</h5>
                        <p>• The license will need at least 2 (two) natural person Fit and Proper Directors of any jurisdiction. <br>
                            • A license shall not appoint a director or senior officer without the prior written approval of the Authority. <br>
                            • The director must pass the fit and proper criteria</p>

                        <h5>Shareholder requirements</h5>
                        <p>• The license will need at least one shareholder. <br>
                            • Corporate shareholding is allowed and 100% foreign ownership is permitted.</p>

                        <h5>Authorized Representative</h5>
                        <p>Unless the license has a significant management presence in the Bermuda,
                            it will be required to appoint a local “authorized representative”,
                            which will be an individual certified by the Bermuda Monetary Authority for this purpose.</p>

                        <h5>Local Office</h5>
                        <p>A license shall not, without the prior written approval of the Authority: <br>
                            a) open, maintain or carry on business through a branch or a representative or contact office outside the Bermuda; or <br>
                            b) incorporate, form or acquire a subsidiary.</p>

                        <h5>Auditor</h5>
                        <p>The relevant licensee shall appoint and at all times have an auditor for the purposes of auditing its financial statements. The auditor must be: <br>
