                     <h3> Cayman Islands Corporate Services </h3>
                    <h4>Introduction </h4>
                    <ol>
                        <h4>Type of company</h4>
                        <li><strong>Associations Not for Profit</strong>
                            <p>Where any association is about to be formed as a limited company, if it is proved to the satisfaction of the Governor that it is to be formed for the purpose of promoting commerce, art, science, religion, charity or any other useful object, and that it is the intention of such association to apply the profits, if any, or other income of the association in promoting its objects, and to prohibit the payment of any dividend to the members of the association such association may be registered pursuant to Section 80 of the Companies Law with limited liability without the addition of the word “limited” to its name. An association not for profit is not required to publish its name, or to send a list of its members, to the Registrar or to pay an annual fee.

                                Such companies, if they meet the legal definition of a Non-Profit Organisations (NPO) are required to further register as NPOs under the Non-profit Organisations Law, 2017.
                            </p>

                        </li>
                        <li><strong>Exempted Limited Duration Company</strong>
                            <p>This form of exempted company, while preserving the limited liability to its members if desired, offers the possibility in certain jurisdictions of advantageous treatment as a partnership. An LDC continues until the terminal time or event specified in its Memorandum of Association. However, its duration must not exceed 30 years and it must have at least two members. Upon its duration expiring, it is deemed to have automatically commenced voluntary winding up and dissolution. It may, however, be wound up earlier voluntarily if the members pass a special resolution to that effect. Compulsory liquidation remains available to creditors of the LDC and also for its members in the circumstances applicable to other companies incorporated under the Companies Law.
                            </p>
                            <ul>
                                <p>Special Features</p>
                                <li>• The Articles of Association of an LDC may provide that the transfer of any share of a member requires the unanimous resolution of all other members.</li>
                                <li>• The Articles of Association of an LDC may provide that the management of the company is vested in the members who are then to be considered as the directors of the company. However, those members can delegate management to a board of directors.</li>
                                <li>• The name of the company must end with “Limited Duration Company” or “LDC”. The registration fee is CI$200, plus the normal exempted company registration fee. The annual fee is the same as that for exempted companies.</li>
                            </ul>
                        </li>
                        <li><strong>Segregated Portfolio Company</strong>
                            <p>The Segregated Portfolio Company (SPC) is a form of exempted company. The Companies Law, PART XIV, provides for any exempted company, a company by way of continuation and an exempted limited duration company to re-register as a segregated portfolio company. This company is required to include in its name either “Segregated Portfolio Company” or “SPC”. An SPC allows for the segregation of the assets and liabilities of individual portfolios – Known in some parts of the world as “cells” – from the general assets of the overall company as well as from other portfolios. Each portfolio, however, is not seen as a separate legal entity.

                                In addition to the annual return required for an exempted company, the segregated portfolio company is required to file a return stating all movements on its portfolio during the year.
                            </p>

                        </li>
                        <li><strong>Foundation Company</strong>
                            <p>A foundation company is a separate Legal entity which may be formed by any person (the “founder”) for any lawful object, which need not be beneficial to other persons, provided it falls within the wide parameters permitted by the legislation (Foundation Companies Law, Companies Law, etc.). Its constitutive documents are its memorandum and articles of association.</p>
                            <p>It should be noted that the Foundations Law is not a stand-alone statute: effectively, it operates as an addition to the Companies Law which will apply to all Cayman Foundations with necessary modifications.

                                An existing Cayman company may be converted to a Cayman Foundation, or an entirely new Cayman Foundation may be created. Such companies must be limited by shares or by guarantee, but can be established with or without share capital. Foundations are prohibited from paying dividends or other distributions of profits or assets to its members or proposed members.

                            </p>
                            <p>The secretary of the Foundation Company must be a “qualified person” meaning a person who is licensed or permitted by the Companies Management Law (2003 Revision) to provide company management services in the Islands.

                                Its constitution must provide, directly or by reference to its articles, for the disposal of any surplus assets the company may have on winding-up.
                            </p>
                        </li>
                        <li><strong>Limited Liability Company</strong>
                            <p>Where the proposed activities of a company are to be carried out mainly outside of the Cayman Islands – offshore – the registrants can apply for registration as a Limited Liability Company (“LLC”) under the Limited Liability Companies Law. Like companies under the Companies Law, this type of company has separate legal identity therefore the members of the company cannot be held personally liable for the company’s debts or liabilities.

                                Unlike Companies under the Companies Law, an LLC has no share capital but members acquire LLC interest. The management of an LLC rests with its members and/or managers.

                                The formation of an LLC is similar to the process of forming a Cayman Islands exempted limited partnership. The filing of a signed registration statement containing certain prescribed information and payment of the registration fee is required in order to register an LLC with the Registrar of Limited Liability Companies. When a change occurs in any matter specified in an LLC’s Registration Statement, the LLC must file a certificate of amendment to the Registration Statement.

                                The LLC Law makes provision for an existing Cayman Islands exempted company to merge with, consolidate with or convert to an LLC and permits non-Cayman Islands entities to re-register and continue into the Cayman Islands as a LLC.

                                In January of each year, the company must file with the Registrar a return certifying that during the previous calendar year it has complied with the provisions of the LLC Law and pay to the Registrar a prescribed annual fee.
                            </p>
                        </li>
                        <li><strong>Non-Resident Company</strong>
                            <p>An ordinary non-resident company is one that has been granted non-resident status through an application to the Minister of Finance through the Registrar of Companies and must state that the company does not intend to carry on business within the Cayman Islands. The company may deal in shares of exempted companies, foreign corporations and partnerships, but may only carry on such other business in the Cayman Islands as is necessary for the furtherance of its foreign business.

                                Such companies must maintain at their registered office, open for public inspection, a register of their past and present members

                                They must also report annually to the Registrar, giving the names and addresses of members, directors and the amount of paid-up capital.

                                A Non-resident company may convert to an ordinary resident company or to an exempted Company.

                                This type of company is required to have its registered office with a CIMA licensed service provider.
                            </p>
                        </li>
                        <li><strong>Overseas Company</strong>
                            <p>An overseas company (usually referred to as a foreign company) is a company incorporated outside the Cayman Islands. Registration pursuant to Part IX of the Companies Law is necessary to enable overseas companies to hold land or carry on business in the Cayman Islands, or to act as the general partner of a Cayman Islands Exempted Limited Partnership.</p>
                        </li>
                        <li><strong>Resident Company</strong>
                            <p>An ordinary resident company carries on business within the Cayman Islands. Resident companies must maintain at their registered office, open for public inspection, a register of their past and present members. They must also report annually to the Registrar, giving the names and addresses of members, directors, and the amounts of paid-up capital. This type of company is also allowed to hold land as defined under the Companies Law.</p>
                        </li>
                        <li><strong>Special Economic Zone Company</strong>
                            <p>A special economic zone company is uthorized to carry on business in a special economic zone pursuant to any Law in force in the Islands. The Companies Law (PART VIIIA) permits the re-registration of existing exempt companies as special economic zone companies. The memorandum of association of such companies is required to state the intention of carrying on special economic zone business. The name of the company must include “special economic zone company” or “SEZC”

                                Should such companies cease to operate in the special economic zone they may remain on the register as exempted companies upon re-registration or opt to be removed from the register.
                            </p>
                        </li>
                        <li><strong>Unlimited Company</strong>
                            <p>In the case of an unlimited company the liability of all its shareholders or members is unlimited. Its members or shareholders have a joint, several and non-limited obligation to meet any deficiency in the assets of the company to settle outstanding debts in the event of the winding up of the company. The articles of such companies must state the number of members with which the company proposes to be registered and, if the company has a share capital, the amount of share capital with which the company proposes to be registered.</p>
                        </li>
                    </ol>

