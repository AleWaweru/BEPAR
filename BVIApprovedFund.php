                    <ol>
                        <p>A BVI Approved fund is a low cost hedge fund vehicle which allows a manager to bring together a small number of investors. This fund is cost-effective and designed to avoid the regulatory burden when establishing a small fund. BVI Approved Funds have the following characteristics: </p>
                        <ul>
                            <li> Has no restriction on the minimum amount each investor subscribes.</li>
                            <li> The total number of investors is limited to 20. </li>
